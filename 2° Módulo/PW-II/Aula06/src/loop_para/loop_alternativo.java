package loop_para;


public class loop_alternativo {
    public static void main(String[] args) {
        
        
        
        
        for(int i = 0, u = 1; i <= 100 && u <= 100; i = i + 2, u = u + 2)  {
           
            System.out.println("i = " + i + " :: " + "u = " + u);
            
            
        }
        
    }
}
