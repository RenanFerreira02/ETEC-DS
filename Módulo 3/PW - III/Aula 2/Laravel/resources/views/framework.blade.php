		<section class="introducao">
			<h1 class="tittle-framework">Framework</h1>

			<p class="paragrafo">
				Um framework é uma estrutura que serve de base para a construção de aplicações web de
				finalidade específica cujo desenvolvimento pode ser muito custoso e/ou problemático. Com um
				framework é possível construir sites, aplicativos e softwares a partir de um esqueleto
				pré-definido, alterando apenas demais particularidades. Os frameworks podem ser front-end ou
				back-end.
			</p>

			<img src="{{url('images/frameworks.png')}}" alt="" width="500" class="imagem" />
		</section>

		<section class="funcao-framework">
			<h3 class="subtittle">Para que serve?</h3>

			<p class="paragrafo">
				Um framework serve para auxiliar o programador ou programadora na hora de construir uma
				aplicação web que já foi amplamente utilizada por outros especialistas.
			</p>

			<p class="paragrafo">
				Por exemplo, na hora de desenvolver um site de vendas, ou um e-commerce, o que seria mais
				vantajoso:
			</p>

			<ul class="list">
				<li>Construir todo o site do zero, inclusive toda sua estrutura logística, ou</li>

				<li>
					Fazer uso de um framework para lojas virtuais que já vem com todo suporte de
					funcionalidades e comandos previamente desenvolvido, faltando as especificações técnicas
					relativas à loja que o utilizará?
				</li>
			</ul>

			<p class="paragrafo">
				A opção 2 é a que traz mais benefícios, não é mesmo? Pois bem, é para isso que serve um
				framework: para que uma aplicação possa ser desenvolvida de maneira mais ágil, rápida e sem
				obstáculos relacionados a problemas que já foram resolvidos por outros profissionais.
			</p>
		</section>

		<section class="vantagens-desvantagens">
			<h3 class="subtittle">Vantagens</h3>

			<p class="paragrafo">
				As vantagens de utilizar um framework são inúmeras, desde uma maior otimização de tempo até
				o impacto positivo em todos os setores de uma companhia, uma vez que o tempo dos
				profissionais gastos em códigos reescritos pode ser voltado à inovação, aqui vai uma lista
				de algumas vantagens.
			</p>

			<ul class="list">
				<li>
					Aumento da produtividade: Um tempo que deixa de ser gasto desnecessariamente dá a um
					profissional a chance de aplicá-lo a algo que pode estar necessitando de uma maior
					atenção. Ao construir aplicações com maior rapidez e fluidez, a produtividade de um
					programador ou programadora também se beneficia, o tornando apto a fazer mais em menor
					tempo menor sem abrir mão da qualidade de seu trabalho.
				</li>

				<li>
					Fácil manutenção: Um framework é um código padronizado, portanto há uma manutenção
					padronizada também. Na hora de fazer uma melhoria ou consertar algum ponto de atenção, não
					será necessário quebrar a cabeça em busca de uma solução desconhecida: basta acessar a
					documentação do framework e encontrar a resposta.
				</li>

				<li>
					Maior segurança: Com uma menor chance de erros de execução e uma manutenção simples, os
					frameworks são tecnologias que garantem mais segurança ao desenvolvimento de softwares,
					tanto para os profissionais de TI quanto para seus clientes.
				</li>

				<li>
					Reduz custos: Se tempo é dinheiro, a economia de tempo também pode ser encarada como uma
					economia financeira. Ao obter um melhor aproveitamento do time de profissionais e de sua
					produtividade, os custos de manutenção de um projeto também caem.
				</li>
			</ul>

			<p class="paragrafo">
				Sob o ponto de vista comercial, esta é uma grande vantagem competitiva: enquanto seu
				concorrente é capaz de entregar 2 projetos ao ano, você será capaz de entregar 4.
			</p>

			<h3 class="subtittle">Desvantagens</h3>

			<p class="paragrafo">
				Como em tecnologia da informação nem tudo são flores, os frameworks também possuem algumas
				desvantagens técnicas, que são:
			</p>

			<ul class="list">
				<li>
					Causa dependência: Os frameworks devem ser utilizados por profissionais experientes que já
					possuam um razoável grau de familiaridade tanto com a aplicação desenvolvida quanto com a
					linguagem de programação que este framework usa. Isso porque, na posição de estudante,
					ingressar no mercado de trabalho muito habituado ao uso de frameworks pode te impedir de
					adquirir importantes capacidades, principalmente àquelas relacionadas à resolução de
					problemas. Também é importante que jovens profissionais conheçam o caminho das pedras do
					desenvolvimento web para que, no futuro, possam ter uma visão mais ampla de seus projetos.
				</li>

				<li>
					Dificuldade em configurar: Por utilizar códigos fixos e imutáveis, muitas vezes os
					frameworks podem apresentar algumas dificuldades de configuração. Isso acontece,
					principalmente, quando os profissionais envolvidos em seu uso não possuem familiaridade
					com suas tecnologias. Caso este obstáculo se apresente, recomendamos a leitura atenta da
					documentação deste framework e a interação com outros profissionais da comunidade.
				</li>

				<li>
					Mais códigos que o necessário: Pode acontecer, também, de utilizarmos um framework inteiro
					para desenvolver uma aplicação que precisa somente de uma parte das funções oferecidas por
					ele. Neste caso, os códigos que "sobrarem" poderão acarretar um peso desnecessário ao
					projeto. Se a aplicação desenvolvida for um site, por exemplo, é preciso tomar cuidado
					para que este código adicional não represente maior tempo de carregamento das páginas.
				</li>
			</ul>
